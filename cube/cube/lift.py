class lift():
    def __init__(self, x, y, z, d):
        self.x = x
        self.y = y
        self.z = z
        self.d = d
        
    def print_lift(self):
        print('x: ' + str(self.x) + 
              ' y: ' + str(self.y) + 
              ' z: ' + str(self.z) + 
              ' d: ' + str(self.d)) 
